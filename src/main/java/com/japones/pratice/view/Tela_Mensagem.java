    private void jButtonVoltarActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButtonVoltarActionPerformed
        if(!praticar_tudo)
        	new Tela_Iniciante_Escolher_Exercicio().main(null);
        else {
        	switch (contexto) {
				case "tre_t": 
					Tela_Iniciante_Treino_Revisao_Escrita.setContexto("t");
					new Tela_Iniciante_Treino_Revisao_Escrita().main(null);
					break;
				
				case "tre_r":
					Tela_Iniciante_Treino_Revisao_Escrita.setContexto("r");
					new Tela_Iniciante_Treino_Revisao_Escrita().main(null);
					break;
					
				case "trl_t":
					Tela_Iniciante_Treino_Revisao_Leitura.setContexto("t");
					new Tela_Iniciante_Treino_Revisao_Leitura().main(null);
					break;
					
				case "trl_r":
					Tela_Iniciante_Treino_Revisao_Leitura.setContexto("r");
					new Tela_Iniciante_Treino_Revisao_Leitura().main(null);
					break;
					
				case "r2h", "r2k":
					Tela_Iniciante_Converter.setContexto(contexto);
					new Tela_Iniciante_Converter().main(null);
					break;
					
				case "h2r", "k2r":
					
					Tela_Iniciante_Converter.setContexto(contexto);
					new Tela_Iniciante_Converter().main(null);
					break;
					
				case "h2r_i", "k2r_i":
					new Tela_Intermediario_Converter().main(null);
					break;
			}
        }
        	
        this.dispose();
    }//GEN-LAST:event_jButtonVoltarActionPerformed

