package com.japones.pratice.model;

public class Caractere {
	
	private String romaji;
	private String hiragana;
	private String katakana;
	private String hiragana_image;
	private String katakana_image;
	private int atividade;
	public String getRomaji() {
		return romaji;
	}
	public void setRomaji(String romaji) {
		this.romaji = romaji;
	}
	public String getHiragana() {
		return hiragana;
	}
	public void setHiragana(String hiragana) {
		this.hiragana = hiragana;
	}
	public String getKatakana() {
		return katakana;
	}
	public void setKatakana(String katakana) {
		this.katakana = katakana;
	}
	public String getHiragana_image() {
		return hiragana_image;
	}
	public void setHiragana_image(String hiragana_image) {
		this.hiragana_image = hiragana_image;
	}
	public String getKatakana_image() {
		return katakana_image;
	}
	public void setKatakana_image(String katakana_image) {
		this.katakana_image = katakana_image;
	}
	public int getAtividade() {
		return atividade;
	}
	public void setAtividade(int atividade) {
		this.atividade = atividade;
	}
	
	
	
	

}
